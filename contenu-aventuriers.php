<div class="contenu-projets">
	<p>Création du logo du Podcast des transitions aventureuses "Les Nouveaux Aventuriers". Humaniste et engagé, ce podcast donne la parole à des femmes et des hommes qui se sont lancé dans une nouvelle aventure, un projet qui leur tenait à coeur. Le créateur de ce podcast souhaitait un logo à leur image, frais et dynamique évoquant autant la nature que l'aventure. Les jeux de typographies du logo permettent d'évoquer à la fois les ondes du podcast, mais aussi la crête d'une montagne et les transitions vécues par les intervenants..</p>

